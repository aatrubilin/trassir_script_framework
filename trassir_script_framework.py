        self._tbot_api = TBotAPI()  # pylint: disable=E0602
        if telegram_ids is not None:
            self.telegram_ids = TBotAPI.prepare_users(  # pylint: disable=E0602
                telegram_ids
            )
        else:
            self.telegram_ids = None


        """ :obj:`datetime`   trassir timestamp

        Args:
            dt (:obj:`datetime`): Datetime

        Returns:
            :obj:`int`: Trassir timestamp
        """
        return int(time.mktime(dt.timetuple())) * 1e6 + dt.microsecond


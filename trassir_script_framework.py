class ScriptError(Exception):
    """Base script exception"""

    _host_api = host

    def __init__(self, *args):
        super(ScriptError, self).__init__(*args)
        self._host_api.timeout(1, self.rise_from_thread)

    def rise_from_thread(self):
        raise self


